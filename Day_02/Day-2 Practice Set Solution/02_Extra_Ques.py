'Write a Python script to calculate the square and cube of a number using variables.'
 
# Prompt the user to enter a number
a = int(input("Enter a number to calculate its square and cube: "))

# Calculate the square of the number
square = a ** 2

# Calculate the cube of the number
cube = a ** 3

# Print the results
print("Square of the number:", square)
print("Cube of the number:", cube)