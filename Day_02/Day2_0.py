# Variables And DataTypes

# Variables are like containers to store values

# Example

a = 10       # 'a' is an integer
b = 25.2     # 'b' is a float
c = "PYTHON" # 'c' is a string
d = True     # 'd' is a boolean
e = None     # 'e' is None

# type() function is used to check the datatype of a variable
ax = type(a)
bx = type(b)
cx = type(c)
dx = type(d)
ex = type(e)

# Print all Information
print(a, "is", ax)
print(b, "is", bx)
print(c, "is", cx)
print(d, "is", dx)
print(e, "is", ex)

# Here, 'a', 'b', 'c', 'd', 'e' are identifiers.
# In a variable name, there cannot be whitespace.
