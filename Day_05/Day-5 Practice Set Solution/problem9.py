# Creating a set with mixed types including a list
s = {7, 8, 12, "Python", [1, 2]}  # The list [1, 2] cannot be directly included in a set
print(len(s))  # Printing the length of the set
print(s)       # Printing the contents of the set

# 1. We cannot use a list in a set.
# 2. And we cannot change the value with an index in a set.
