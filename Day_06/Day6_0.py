# If - Else Example

# Input: User's age
a = int(input("Enter your age: "))

# Check if the age is 18 or older
if (a >= 18):
    print("You are allowed to vote")  # Print if the user is eligible to vote
else:
    print("You are not allowed to vote")  # Print if the user is not eligible to vote

print("End of if-else statement")  # Indicate the end of the if-else block



