# Different Loops in Python
# Suppose we have to print the numbers from 1 to 10 directly

print(1)
print(2)
print(3)
print(4)
print(5)
print(6)
print(7)
print(8)
print(9)
print(10)

print('\nPrint 1-10 with the help of loop\n')  # Section break

# Now printing the numbers from 1 to 10 using a loop
