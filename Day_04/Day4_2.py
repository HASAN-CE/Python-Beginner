# Tuple
# A tuple is an immutable data type in Python

a = (1, 2, 3, 4, 5)  # Tuples are enclosed in parentheses
print(type(a))  # Output the type of the tuple

# Empty tuple
b = ()

# Tuple with one element
c = (1,)  # A tuple with a single element requires a trailing comma
print(type(c), c)  # Output the type and content of the tuple

# Tuple with different data types
d = (1, 6181, "PYTHON", True, None)  # Tuple with various data types
print(d)  # Output the tuple
