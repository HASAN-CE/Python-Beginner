'Concatenate two tuples and sort the resulting tuple.'

# Define the first tuple
t1 = (1, 5, 8, 1, 98, 100)

# Define the second tuple
t2 = (65, 17, 851, 915, 102)

# Concatenate the two tuples and sort the resulting tuple
# The + operator is used to concatenate the tuples
# The sorted() function is used to sort the concatenated tuple
print('Concatenate two tuples and sort the resulting tuple:', sorted(t1 + t2))

