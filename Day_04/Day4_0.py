# Lists
# Since lists are mutable, we can change the element at index 0
list1 = ["Helper", "Program", "Cyber", 56, False, None, 468.5, "This"]  # Define a list with various data types
print(list1)  # Output the entire list

# Access the element at the 0th index
print(list1[0])  # Output the element at the first index (0)

# Change the existing list by modifying the 0th index element
list1[0] = "Hacker"  # Change the first element to "Hacker"
print(list1)  # Output the modified list
