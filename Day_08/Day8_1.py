# Types of Functions:
# - Built-in Functions: These are pre-defined functions in Python such as `print()`, `len()`, `range()`, etc.
# - User Defined Functions: These are functions that we define ourselves.

# User-defined function
def func1():
    print('Function')  # This function simply prints 'Function'

# Example of calling the user-defined function
func1()  # Calling the function to execute its code
